/*
=====================================================================================================================================================================
Bài 2: Tính toán giá trị của biểu thức:
Cho ba số nguyên a , b và c, hãy tính S = a(b+c) + b(a+c).
=====================================================================================================================================================================
- Đầu vào:
3 số a, b, c trên 1 dòng.
=====================================================================================================================================================================
- Ràng buộc: 
-10^8 ≤ a, b, c ≤ 10^8
=====================================================================================================================================================================
- Đầu ra:
In ra kết quả cùa biểu thức
=====================================================================================================================================================================
- Input 01:
1 2 3
=====================================================================================================================================================================
- Output 01:
13
=====================================================================================================================================================================
*/
#include <iostream>

using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int a, b, c;
    cin >> a >> b >> c;

    cout << (long long)a * (b + c) + (long long)b * (a + c); 

    return 0;
}
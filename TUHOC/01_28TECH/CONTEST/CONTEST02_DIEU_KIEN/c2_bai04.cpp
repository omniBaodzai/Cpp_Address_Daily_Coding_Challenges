/*
=====================================================================================================================================================================
Bài 4: Chu vi & diện tích hình tròn:
Cho bán kính R của hình tròn. Yêu cầu tính chu vi và diện tích của hình tròn đó.
Lấy PI = 3.14
=====================================================================================================================================================================
- Đầu vào:
1 dòng chứa bán kính R là số nguyên dương.
=====================================================================================================================================================================
- Ràng buộc: 
10 ≤ R ≤ 1000
=====================================================================================================================================================================
- Đầu ra:
In ra chu vi và diện tích trên 1 dòng, kết quả lấy độ chính xác 4 số sau dấu phẩy.
=====================================================================================================================================================================
- Input 01:
10
=====================================================================================================================================================================
- Output 01:
62.8000 314.0000
=====================================================================================================================================================================
*/
#include <iostream>
#include <iomanip>

using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    const double PI = 3.14;

    int r; cin >> r;
    
    cout << fixed << setprecision(4);
    cout << 2 * PI * r << " " << PI * r * r;

    return 0;
}
/*
=====================================================================================================================================================================
Bài 5: Khoảng cách Euclid:
Có nhiều khoảng cách giữa 2 điểm trong hệ tọa độ ví dụ như khoảng cách Manhattan,
Euclid, Minkowski. Nhưng trong chương trình toán phổ thông các bạn sử dụng nhiều
nhất là khoảng cách Euclid. Vì vậy bạn hãy tính khoảng cách Euclid giữa 2 điểm
trong hệ tọa độ Oxy
=====================================================================================================================================================================
- Đầu vào:
Tọa độ của 2 điểm (x1, y1) và (x2, y2) là các số nguyên
=====================================================================================================================================================================
- Ràng buộc: 
-10^6 ≤ xi, yi ≤ 10^6
=====================================================================================================================================================================
- Đầu ra:
In ra khoảng cách giữa 2 điểm, lấy độ chính xác 2 số sau dấu phẩy.
=====================================================================================================================================================================
- Input 01:
1 4 4 8
=====================================================================================================================================================================
- Output 01:
5.00
=====================================================================================================================================================================
*/
#include <iostream>
#include <iomanip>
#include <cmath>

using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int x1, y1, x2, y2;
    cin >> x1 >> y1 >> x2 >> y2;

    cout << fixed << setprecision(2);
    cout << sqrt((x2 - x1) * (x2 - x1) + (y2 - y1) * (y2 - y1));

    return 0;
}
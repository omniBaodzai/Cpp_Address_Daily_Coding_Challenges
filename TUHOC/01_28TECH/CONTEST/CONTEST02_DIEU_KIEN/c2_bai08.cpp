/*
=====================================================================================================================================================================
Bài 8: Tổng, hiệu, tích, thương:
Nhập vào 2 số nguyên, in ra tổng, hiệu, tích, thương (lấy độ chính xác với 4 chữ
số) của 2 số đó.
=====================================================================================================================================================================
- Đầu vào:
2 số nguyên a, b trên 1 dòng.
=====================================================================================================================================================================
- Ràng buộc: 
-10^8 < = a, b <= 10^8
=====================================================================================================================================================================
- Đầu ra:
In ra tổng, hiệu, tích, thương trên từng dòng. Nếu trường hợp không thể tìm được
thương của 2 số thì sẽ in ra "INVALID" cho dòng kết quả của thương.
=====================================================================================================================================================================
- Input 01:
7769 0
=====================================================================================================================================================================
- Output 01:
7769
7769
0
INVALID
=====================================================================================================================================================================
*/
#include <iostream>
#include <iomanip>

using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int a, b;
    cin >> a >> b;

    cout << a + b << "\n";
    cout << a - b << "\n";
    cout << 1ll * a * b << "\n";
    
    if (b == 0)
        cout << "INVALID" << "\n";
    else 
        cout << fixed << setprecision(4) << 1.0 * a / b << "\n";

    return 0;
}